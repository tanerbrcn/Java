package Lesson2Examples.ReCapDemo_Classes;

public class FourTransactions {

	public int toCollect(int number1,int number2) { //Toplama
		return number1+number2;
	}
	
	public int takeOut(int number1,int number2) { //Çıkartma
		return number1-number2;
	}
	
	public int divide(int number1,int number2) { //Bölme
		return number1/number2;
	}
	
	public int bump(int number1,int number2) { //Çarpma
		return number1*number2;
	}
	
	
}
